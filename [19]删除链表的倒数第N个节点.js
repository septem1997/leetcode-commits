var removeNthFromEnd = function(head, n) {
    return searchNode(head,n)===-1?head:head.next
};
let searchNode = (node,n)=>{
    if (node===null){
        return 0
    }
    const count = searchNode(node.next,n)
    if (count===n){
        if (n===1){
            node.next = null
        }else {
            node.next = node.next.next
        }
        return -1
    }else if (count===-1) {
        return -1
    }
    return count + 1
}
