/**
 * @param {string} s
 * @param {string} t
 * @return {boolean}
 */
var isAnagram = function(s, t) {
    const sMap = {}
    if (s.length!==t.length){
        return false
    }
    for (let i = 0;i<s.length;i++){
        if (sMap[s[i]]){
            sMap[s[i]]++
        }else {
            sMap[s[i]] = 1
        }
    }
    for (const c of t){
        if (sMap[c]){
            sMap[c]--
        }
    }
    for (let key in sMap){
        if (sMap[key]!==0){
            return false
        }
    }
    return true
};
